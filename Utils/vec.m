function out = vec(X)
% =======================================================================
% Vectorize the matrix X
% =======================================================================
% out = vec(X)
% -----------------------------------------------------------------------
% INPUT
%   - X: [r x c] matrix
% -----------------------------------------------------------------------
% OUTPUT
%   - out: [r*c x 1] matrix
% =======================================================================
% Ambrogio Cesa Bianchi, March 2015
% ambrogio.cesabianchi@gmail.com

[r,c] = size(X);
out = reshape(X,r*c,1);